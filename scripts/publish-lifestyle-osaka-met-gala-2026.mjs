function norm(value = '') {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, ' ').trim();
}

function isDuplicateCluster(article) {
  const text = norm([article.slug, article.title, article.excerpt, article.body].filter(Boolean).join(' '));
  const hasUsOpenTravel = text.includes('us open') && (
    text.includes('travel package') ||
    text.includes('hotel package') ||
    text.includes('hospitality') ||
    text.includes('new york travel') ||
    text.includes('flushing meadows package')
  );
  const hasSameSearchIntent = text.includes('us open 2026') && (
    text.includes('ticket') ||
    text.includes('where to stay') ||
    text.includes('fan experience')
  );
  return hasUsOpenTravel || hasSameSearchIntent;
}

async function insertArticle(article) {
  const first = await supabase.from('articles').insert(article);
  if (!first.error) return;

  const msg = first.error.message || '';
  if (first.error.code !== '23505' && !msg.includes('duplicate key value violates unique constraint "articles_pkey"')) {
    throw first.error;
  }

  const { data: maxRows, error: maxError } = await supabase
    .from('articles')
    .select('id')
    .order('id', { ascending: false })
    .limit(1);
  if (maxError) throw maxError;

  const nextId = ((maxRows?.[0]?.id || 0) + 1);
  const retry = await supabase.from('articles').insert({ id: nextId, ...article });
  if (retry.error) throw retry.error;
}

